# OPERADORES DE COMPARAÇÃO


# OPERADOR                NOME                FUNÇÃO
# ==                      IGUAL              VERIFICA SE UM VALOR É IGUAL AO OUTRO
# !=                      DIFERENTE          VERIFICA SE UM VALOR É DIFERENTE DO OUTRO
# >                       MAIOR QUE          VERIFICA SE UM VALOR É MAIOR QUE OUTRO
# >=                      MAIOR OU IGUAL     VERIFICA SE UM VALOR É MAIOR OU IGUAL A OUTRO
# <                       MENOR QUE          VERIFICA SE UM VALOR É MENOR QUE OUTRO
# <=                      MENOR OU IGUAL     VERIFICA SE UM VALOR É MENOR OU IGUAL AO OUTRO



# IGUAL
a = 3
b = "3"
print(a == b) # false

# DIFERENTE
c = 4
d = "4"
print(c != d) # true


# MAIOR
e = 5
f = 10
print(e > f) # false


# MAIOR OU IGUAL
g = 12
h = 12
print(g >= h) # true


# MENOR QUE
i = 13
j = 12
print(i < j) # false

# MENOR OU IGAUL
l = 15
m = 145
print(l <= m) # true