# Tipos de variáveis(primitivos)

# STR - String(texto)
nome2 = 'Paulo'
# INT - Inteiro(números inteiros)
idade2 = 33
# Float - Número decimais
peso = 80.1
# Boolean - Verdadeiro ou Falso
sexo_masculino = True


# Imprimido valores
print(nome2)
print(idade2)
print(peso)
print(sexo_masculino)


# operadores Aritmeticos
print(2 + 2) # Adição - Retorna a soma de ambos operadores
print(10 - 2) # Subtração - Retorna a subtração de ambos operadores
print(10 * 2) # Multipli

